package br.com.gestao.salao.vo;

public class UsuarioAndroidVO {

	private Integer codigo;
	private String nome;
	private String numeroCeluar;
	private String email;

	public UsuarioAndroidVO(){
		
	}
	
	public Integer getCodigo() {
		return codigo;
	}

	public void setCodigo(Integer codigo) {
		this.codigo = codigo;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getNumeroCeluar() {
		return numeroCeluar;
	}

	public void setNumeroCeluar(String numeroCeluar) {
		this.numeroCeluar = numeroCeluar;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	
	
	
}
